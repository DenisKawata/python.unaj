# Definir una función denominada IMPRIMO_FECHA que reciba tres cadenas de carateres como parámetros formales, que representan un día, un mes y un año e imprima la fecha de la siguiente manera: "21 de Septembre de 2021".

def imprimir_fecha(dia, mes, anio):
    fecha = (dia + " de " + mes + " de " + anio)

    print(fecha)

imprimir_fecha("21", "sepriembre", "2021")

# En este código, la función IMPRIMO_FECHA recibe tres parámetros formales, que representan un día, un mes y un año. Dentro de la función, se utiliza el operador + para concatenar las cadenas de caracteres de los parámetros formales. La función devuelve la fecha en formato "21 de Septembre de 2021". 


